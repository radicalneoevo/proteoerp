	function ultimos(){
		$mensaje = '';
		$usuario = $this->db->escape($this->secu->usuario());
		$mSQL = "SELECT fecha, hora, modulo, comenta FROM logusu WHERE usuario=".$usuario." AND modulo NOT IN ('MENU','PRINCIPAL') ORDER BY id DESC LIMIT 10";
		$query = $this->db->query($mSQL);
		if ( $query->num_rows() > 0 ) {
			foreach( $query->result() as $row ) {
				$mensaje = "<div class='msgln'>".substr($row->fecha,8,2)."-".substr($row->hora,0,5)." <b>".strtoupper($row->modulo)."</b>: ".$row->comenta."<br></div>\n".$mensaje;
			}
		}
		echo $mensaje;
	}

	function instalar(){
		if (!$this->db->table_exists('logusu')) {
			$mSQL="CREATE TABLE `logusu` (
			  `usuario` varchar(12) NOT NULL DEFAULT '',
			  `fecha` date NOT NULL DEFAULT '0000-00-00',
			  `hora` time NOT NULL DEFAULT '00:00:00',
			  `modulo` varchar(20) DEFAULT NULL,
			  `comenta` text
			) ENGINE=MyISAM DEFAULT CHARSET=latin1";
			$this->db->query($mSQL);
		}
	}
