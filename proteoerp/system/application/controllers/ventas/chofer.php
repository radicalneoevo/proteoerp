				$check = $this->datasis->dameval("SELECT count(*) FROM chofer WHERE $mcodp=".$this->db->escape($data[$mcodp]));
				if ( $check == 0 ){
					$this->db->insert('chofer', $data);
					echo "Registro Agregado";

