				echo " El registro no puede ser eliminado; tiene movimiento ";
			} else {
				$this->db->simple_query("DELETE FROM obco WHERE id=$id ");
				logusu('OBCO',"Registro ????? ELIMINADO");
				echo "Registro Eliminado";
			}
		};
	}

