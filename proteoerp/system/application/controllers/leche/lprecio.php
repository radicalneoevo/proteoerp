				echo " El registro no puede ser eliminado; tiene movimiento ";
			} else {
				$this->db->simple_query("DELETE FROM lprecio WHERE id=$id ");
				logusu('LPRECIO',"Registro ????? ELIMINADO");
				echo "Registro Eliminado";
